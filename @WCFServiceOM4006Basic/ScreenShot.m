function ScreenShotResult = ScreenShot(obj,fileName)
%ScreenShot(obj,fileName)
%
%     Input:
%       fileName = (string)
%   
%     Output:
%       ScreenShotResult = (boolean)

% Build up the argument lists.
values = { ...
   fileName, ...
   };
names = { ...
   'fileName', ...
   };
types = { ...
   '{http://www.w3.org/2001/XMLSchema}string', ...
   };

% Create the message, make the call, and convert the response into a variable.
soapMessage = createSoapMessage( ...
    'http://tempuri.org/', ...
    'ScreenShot', ...
    values,names,types,'document');
response = callSoapService( ...
    obj.endpoint, ...
    'http://tempuri.org/IWCFServiceOM4006Basic/ScreenShot', ...
    soapMessage);
ScreenShotResult = parseSoapResponse(response);
